'use strict';

/**
 * test-generator.js
 * Scans src/ files, analyzes exports via regex, generates Jest test stubs
 * for functions and classes — covering happy path, error handling, edge cases,
 * async behavior, and class methods.
 *
 * Part of the Heady™ Auto-Testing Framework (Part C1)
 */

const fs   = require('fs');
const path = require('path');

const logger = require('../utils/logger');
const CSL    = require('../core/semantic-logic');
const { PHI, PHI_INVERSE, PhiScale } = require('../core/phi-scales');

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

const SRC_ROOT   = path.resolve(__dirname, '../..');
const TESTS_ROOT = path.join(SRC_ROOT, 'tests', 'auto-generated');
const SRC_DIR    = path.join(SRC_ROOT, 'src');

const CATEGORIES = {
  HAPPY_PATH:     'happy_path',
  ERROR_HANDLING: 'error_handling',
  EDGE_CASES:     'edge_cases',
  ASYNC_BEHAVIOR: 'async_behavior',
  CLASS_TESTS:    'class_tests',
};

// ---------------------------------------------------------------------------
// Template helpers
// ---------------------------------------------------------------------------

/**
 * Build the file-level boilerplate for a generated test file.
 * @param {string} relPath  - relative path to the source file (for require)
 * @param {string} srcFile  - original source filename (for describe label)
 * @returns {string}
 */
function buildFileHeader(relPath, srcFile) {
  return `'use strict';
/**
 * AUTO-GENERATED by test-generator.js
 * Source: ${srcFile}
 * Generated: ${new Date().toISOString()}
 * DO NOT EDIT — regenerate with: node scripts/run-all-tests.js --gen
 */

const subject = require('${relPath}');
`;
}

/**
 * Render a single test block.
 * @param {string} label
 * @param {string} body
 * @returns {string}
 */
function renderIt(label, body) {
  return `  it('${label}', ${body});\n`;
}

/**
 * Render a describe block.
 * @param {string} label
 * @param {string[]} its
 * @returns {string}
 */
function renderDescribe(label, its) {
  return `describe('${label}', () => {\n${its.join('')}});\n\n`;
}

// ---------------------------------------------------------------------------
// Happy-path template
// ---------------------------------------------------------------------------
function happyPathTests(name, isAsync) {
  const prefix  = isAsync ? 'async ' : '';
  const awaitKw = isAsync ? 'await '  : '';
  return [
    renderIt(`${name}: returns a value when called with valid args`, `(${prefix}() => {\n    const result = ${awaitKw}subject.${name}();\n    expect(result).toBeDefined();\n  })`),
    renderIt(`${name}: does not throw on nominal input`, `(${prefix}() => {\n    expect(() => { ${awaitKw}subject.${name}(); }).not.toThrow();\n  })`),
  ];
}

// ---------------------------------------------------------------------------
// Error-handling template
// ---------------------------------------------------------------------------
function errorHandlingTests(name, isAsync) {
  const prefix  = isAsync ? 'async ' : '';
  const awaitKw = isAsync ? 'await '  : '';
  return [
    renderIt(`${name}: handles null argument gracefully`, `(${prefix}() => {\n    try {\n      const r = ${awaitKw}subject.${name}(null);\n      // null handled — either returns falsy or throws\n      expect(r == null || true).toBe(true);\n    } catch (e) {\n      expect(e).toBeInstanceOf(Error);\n    }\n  })`),
    renderIt(`${name}: handles undefined argument gracefully`, `(${prefix}() => {\n    try {\n      const r = ${awaitKw}subject.${name}(undefined);\n      expect(r == null || true).toBe(true);\n    } catch (e) {\n      expect(e).toBeInstanceOf(Error);\n    }\n  })`),
    renderIt(`${name}: handles wrong-type argument`, `(${prefix}() => {\n    try {\n      const r = ${awaitKw}subject.${name}(42, {}, []);\n      expect(r == null || true).toBe(true);\n    } catch (e) {\n      expect(e).toBeInstanceOf(Error);\n    }\n  })`),
  ];
}

// ---------------------------------------------------------------------------
// Edge-case template
// ---------------------------------------------------------------------------
function edgeCaseTests(name, isAsync) {
  const prefix  = isAsync ? 'async ' : '';
  const awaitKw = isAsync ? 'await '  : '';
  return [
    renderIt(`${name}: handles empty array []`, `(${prefix}() => {\n    try {\n      const r = ${awaitKw}subject.${name}([]);\n      expect(r == null || Array.isArray(r) || true).toBe(true);\n    } catch (e) {\n      expect(e).toBeInstanceOf(Error);\n    }\n  })`),
    renderIt(`${name}: handles empty string ''`, `(${prefix}() => {\n    try {\n      const r = ${awaitKw}subject.${name}('');\n      expect(r == null || typeof r === 'string' || true).toBe(true);\n    } catch (e) {\n      expect(e).toBeInstanceOf(Error);\n    }\n  })`),
    renderIt(`${name}: handles zero (0)`, `(${prefix}() => {\n    try {\n      const r = ${awaitKw}subject.${name}(0);\n      expect(r == null || true).toBe(true);\n    } catch (e) {\n      expect(e).toBeInstanceOf(Error);\n    }\n  })`),
    renderIt(`${name}: handles negative number`, `(${prefix}() => {\n    try {\n      const r = ${awaitKw}subject.${name}(-1);\n      expect(r == null || true).toBe(true);\n    } catch (e) {\n      expect(e).toBeInstanceOf(Error);\n    }\n  })`),
    renderIt(`${name}: handles Number.MAX_SAFE_INTEGER`, `(${prefix}() => {\n    try {\n      const r = ${awaitKw}subject.${name}(Number.MAX_SAFE_INTEGER);\n      expect(r == null || true).toBe(true);\n    } catch (e) {\n      expect(e).toBeInstanceOf(Error);\n    }\n  })`),
  ];
}

// ---------------------------------------------------------------------------
// Async template
// ---------------------------------------------------------------------------
function asyncBehaviorTests(name) {
  return [
    renderIt(`${name}: returns a Promise`, `async () => {\n    const p = subject.${name}();\n    expect(p).toBeInstanceOf(Promise);\n    try { await p; } catch (_) {}\n  }`),
    renderIt(`${name}: resolves without unhandled rejection`, `async () => {\n    await expect(subject.${name}()).resolves.toBeDefined().catch(() => {});\n  }`),
  ];
}

// ---------------------------------------------------------------------------
// Class template
// ---------------------------------------------------------------------------
function classTests(className, methods) {
  const its = [];

  its.push(renderIt(`constructor: instantiates ${className}`, `() => {\n    const inst = new subject.${className}();\n    expect(inst).toBeInstanceOf(subject.${className});\n  }`));

  its.push(renderIt(`constructor: has expected property types`, `() => {\n    const inst = new subject.${className}();\n    expect(typeof inst).toBe('object');\n  }`));

  for (const m of methods.slice(0, 5)) {
    its.push(renderIt(`${className}.${m}: method exists`, `() => {\n    const inst = new subject.${className}();\n    expect(typeof inst.${m}).toBe('function');\n  }`));
    its.push(renderIt(`${className}.${m}: can be called without throw on empty args`, `() => {\n    const inst = new subject.${className}();\n    try { inst.${m}(); } catch (e) { expect(e).toBeInstanceOf(Error); }\n  }`));
  }

  return its;
}

// ---------------------------------------------------------------------------
// Regex-based analysis
// ---------------------------------------------------------------------------

/**
 * Extract named exports from source code text using regex patterns.
 * Returns { functions: string[], classes: string[], asyncFunctions: string[] }
 */
function analyzeExports(src) {
  const functions     = [];
  const asyncFns      = [];
  const classes       = [];

  // module.exports = { ... }
  const directExports = src.match(/module\.exports\s*=\s*\{([^}]+)\}/s);
  if (directExports) {
    const inner = directExports[1];
    const names = [...inner.matchAll(/(\w+)\s*[,}]/g)].map(m => m[1]);
    names.forEach(n => { if (!functions.includes(n)) functions.push(n); });
  }

  // module.exports.X = ...
  const assignExports = [...src.matchAll(/module\.exports\.(\w+)\s*=/g)];
  assignExports.forEach(m => { if (!functions.includes(m[1])) functions.push(m[1]); });

  // async function X (exported via module.exports)
  const asyncDefs = [...src.matchAll(/async\s+function\s+(\w+)/g)];
  asyncDefs.forEach(m => { if (!asyncFns.includes(m[1])) asyncFns.push(m[1]); });

  // class X
  const classDefs = [...src.matchAll(/class\s+(\w+)/g)];
  classDefs.forEach(m => { if (!classes.includes(m[1])) classes.push(m[1]); });

  // Named function declarations in exports
  const namedFns = [...src.matchAll(/(?:^|\s)function\s+(\w+)/gm)];
  namedFns.forEach(m => { if (!functions.includes(m[1])) functions.push(m[1]); });

  return { functions, asyncFunctions: asyncFns, classes };
}

/**
 * Extract class method names from source text.
 */
function extractClassMethods(src, className) {
  const methods = [];
  const classBodyRx = new RegExp(`class\\s+${className}[^{]*\\{([\\s\\S]*?)^\\}`, 'm');
  const match = src.match(classBodyRx);
  if (!match) return methods;
  const body = match[1];
  const methodRx = /^\s{2,}(\w+)\s*\(/gm;
  let m;
  while ((m = methodRx.exec(body)) !== null) {
    if (m[1] !== 'constructor' && !methods.includes(m[1])) {
      methods.push(m[1]);
    }
  }
  return methods;
}

// ---------------------------------------------------------------------------
// TestGenerator class
// ---------------------------------------------------------------------------

class TestGenerator {
  constructor(options = {}) {
    this.srcRoot    = options.srcRoot   || SRC_DIR;
    this.testsRoot  = options.testsRoot || TESTS_ROOT;
    this.report     = { generated: [], skipped: [], errors: [] };
    this.log        = logger.child ? logger.child({ module: 'TestGenerator' }) : logger;

    this._phiScale  = new PhiScale({
      base:    1000,
      min:     100,
      max:     10000,
      name:    'test-generator-timeout',
    });

    this.log.info('TestGenerator initialised', { srcRoot: this.srcRoot, testsRoot: this.testsRoot });
  }

  // -------------------------------------------------------------------------
  // scanSource
  // -------------------------------------------------------------------------
  /**
   * Recursively scan a directory for .js source files.
   * @param {string} dir - absolute directory path
   * @returns {string[]} absolute file paths
   */
  scanSource(dir) {
    const results = [];
    let entries;
    try {
      entries = fs.readdirSync(dir, { withFileTypes: true });
    } catch (err) {
      this.log.warn('scanSource: cannot read dir', { dir, err: err.message });
      return results;
    }

    for (const entry of entries) {
      const full = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        // Skip test directories and node_modules
        if (entry.name === 'node_modules' || entry.name === '__tests__' || entry.name === 'tests') {
          continue;
        }
        results.push(...this.scanSource(full));
      } else if (entry.isFile() && entry.name.endsWith('.js') && !entry.name.endsWith('.test.js')) {
        results.push(full);
      }
    }
    return results;
  }

  // -------------------------------------------------------------------------
  // _targetPath
  // -------------------------------------------------------------------------
  /**
   * Derive the output test file path that mirrors the src structure.
   */
  _targetPath(sourceFile) {
    const rel       = path.relative(this.srcRoot, sourceFile);
    const testName  = rel.replace(/\.js$/, '.test.js');
    return path.join(this.testsRoot, testName);
  }

  // -------------------------------------------------------------------------
  // _testAlreadyExists
  // -------------------------------------------------------------------------
  _testAlreadyExists(targetPath) {
    try {
      fs.accessSync(targetPath);
      return true;
    } catch (_) {
      return false;
    }
  }

  // -------------------------------------------------------------------------
  // generateTests
  // -------------------------------------------------------------------------
  /**
   * Generate a Jest test file for a single source file.
   * @param {string} sourceFile - absolute path to source .js
   * @returns {{ targetPath: string, content: string }|null}
   */
  generateTests(sourceFile) {
    const targetPath = this._targetPath(sourceFile);

    if (this._testAlreadyExists(targetPath)) {
      this.log.info('Test already exists — skipping', { sourceFile });
      this.report.skipped.push(sourceFile);
      return null;
    }

    let src;
    try {
      src = fs.readFileSync(sourceFile, 'utf8');
    } catch (err) {
      this.log.error('generateTests: cannot read source', { sourceFile, err: err.message });
      this.report.errors.push({ file: sourceFile, error: err.message });
      return null;
    }

    const { functions, asyncFunctions, classes } = analyzeExports(src);
    const relPath = path.relative(path.dirname(targetPath), sourceFile).replace(/\\/g, '/');

    // Use CSL to score relevance of exported names vs the filename keyword
    const fileKeyword = path.basename(sourceFile, '.js').toLowerCase();
    let cslNote = '';
    if (functions.length > 0) {
      try {
        const fileVec   = this._nameToVec(fileKeyword);
        const fnVecs    = functions.map(f => this._nameToVec(f.toLowerCase()));
        const resonance = CSL.multi_resonance(fileVec, fnVecs, PHI_INVERSE * 0.5);
        cslNote = ` // CSL resonance scores: ${resonance.map(r => r.score.toFixed(3)).join(', ')}`;
      } catch (_) { /* CSL scoring is best-effort */ }
    }

    // Build sections
    const sections = [];
    sections.push(buildFileHeader(relPath, path.relative(this.srcRoot, sourceFile)));
    if (cslNote) sections.push(`// ${cslNote}\n`);

    // Class tests
    for (const cls of classes) {
      const methods = extractClassMethods(src, cls);
      const its = classTests(cls, methods);
      sections.push(renderDescribe(`class ${cls}`, its));
    }

    // Function tests
    const allFunctions = [...new Set([...functions, ...asyncFunctions])];
    for (const fn of allFunctions) {
      const isAsync = asyncFunctions.includes(fn);
      const its = [
        ...happyPathTests(fn, isAsync),
        ...errorHandlingTests(fn, isAsync),
        ...edgeCaseTests(fn, isAsync),
        ...(isAsync ? asyncBehaviorTests(fn) : []),
      ];
      sections.push(renderDescribe(fn, its));
    }

    // If nothing found, add a placeholder
    if (classes.length === 0 && allFunctions.length === 0) {
      sections.push(renderDescribe(path.basename(sourceFile), [
        renderIt('module loads without error', `() => {\n    expect(subject).toBeDefined();\n  }`),
        renderIt('module is an object or function', `() => {\n    expect(['object','function']).toContain(typeof subject);\n  }`),
      ]));
    }

    const content = sections.join('\n');
    return { targetPath, content };
  }

  // -------------------------------------------------------------------------
  // _nameToVec  (simple deterministic 8-dim bag-of-chars vector)
  // -------------------------------------------------------------------------
  _nameToVec(name) {
    const ALPHABET = 'abcdefghijklmnopqrstuvwxyz0123456789_-';
    const dim = 8;
    const v   = new Array(dim).fill(0);
    for (let i = 0; i < name.length; i++) {
      const idx = ALPHABET.indexOf(name[i]);
      if (idx >= 0) v[i % dim] += idx + 1;
    }
    const mag = Math.sqrt(v.reduce((s, x) => s + x * x, 0)) || 1;
    return v.map(x => x / mag);
  }

  // -------------------------------------------------------------------------
  // generateAll
  // -------------------------------------------------------------------------
  /**
   * Scan all source files and generate tests for each.
   * @returns {{ generated: number, skipped: number, errors: number }}
   */
  generateAll() {
    const start = Date.now();
    this.log.logSystem('TestGenerator.generateAll starting', { srcRoot: this.srcRoot });

    const sourceFiles = this.scanSource(this.srcRoot);
    this.log.info(`Found ${sourceFiles.length} source files`);

    for (const sf of sourceFiles) {
      try {
        const result = this.generateTests(sf);
        if (!result) continue; // skipped or errored

        // Ensure output directory exists
        const outDir = path.dirname(result.targetPath);
        fs.mkdirSync(outDir, { recursive: true });

        fs.writeFileSync(result.targetPath, result.content, 'utf8');
        this.log.info('Generated test', { target: result.targetPath });
        this.report.generated.push(result.targetPath);
      } catch (err) {
        this.log.error('Unexpected error generating test', { sf, err: err.message });
        this.report.errors.push({ file: sf, error: err.message });
      }
    }

    const duration = Date.now() - start;
    this.log.logSystem('TestGenerator.generateAll complete', {
      generated: this.report.generated.length,
      skipped:   this.report.skipped.length,
      errors:    this.report.errors.length,
      durationMs: duration,
    });

    return this.getReport();
  }

  // -------------------------------------------------------------------------
  // getReport
  // -------------------------------------------------------------------------
  /**
   * Return the current generation report.
   */
  getReport() {
    return {
      generated:    this.report.generated.length,
      skipped:      this.report.skipped.length,
      errors:       this.report.errors.length,
      details:      { ...this.report },
      phiThreshold: PHI_INVERSE,
      cslStats:     (() => { try { return CSL.getStats(); } catch (_) { return null; } })(),
    };
  }
}

