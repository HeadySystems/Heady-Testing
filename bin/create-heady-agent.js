// ─── CLI Entry Point ──────────────────────────────────────────────

function main() {
    const args = process.argv.slice(2);

    if (args.length === 0 || args.includes('--help') || args.includes('-h')) {
        console.log(`
  create-heady-agent — Scaffold a new HeadyMCP agent

  Usage:
    npx create-heady-agent <agent-name> [--template <template>]

  Templates:
    basic            Basic CRUD Agent
    ai-assistant     AI Assistant Agent
    data-processing  Data Processing Agent
    integration      REST API Integration Agent

  Options:
    --template, -t   Agent template (default: basic)
    --help, -h       Show help
`);
        return;
    }

    const agentName = args[0];
    let template = 'basic';

    const tIdx = args.indexOf('--template');
    const tShort = args.indexOf('-t');
    if (tIdx >= 0 && args[tIdx + 1]) template = args[tIdx + 1];
    else if (tShort >= 0 && args[tShort + 1]) template = args[tShort + 1];

    if (!TEMPLATES[template]) {
        console.error(`Unknown template: ${template}`);
        console.error(`Available: ${Object.keys(TEMPLATES).join(', ')}`);
        process.exit(1);
    }

    const dir = path.resolve(agentName);
    console.log(`\n  ✨ Creating ${agentName} (${TEMPLATES[template].name})\n`);

    // Create directory structure
    const dirs = ['src', 'src/tools', 'src/resources', '__tests__', '.heady'];
    fs.mkdirSync(dir, { recursive: true });
    for (const d of dirs) fs.mkdirSync(path.join(dir, d), { recursive: true });

    // Generate files
    const files = {
        'package.json': generatePackageJson(agentName, template),
        'src/index.js': generateIndex(agentName, template),
        '__tests__/agent.test.js': generateTest(agentName),
        '.heady/agent.yaml': generateAgentYaml(agentName, template),
        'README.md': generateReadme(agentName, template),
    };

    for (const [filepath, content] of Object.entries(files)) {
        fs.writeFileSync(path.join(dir, filepath), content);
        console.log(`  📄 ${filepath}`);
    }

    // Install dependencies
    console.log('\n  📦 Installing dependencies...\n');
    try {
        execSync('npm install', { cwd: dir, stdio: 'inherit' });
    } catch (e) {
        console.log('  ⚠ npm install failed — run manually');
    }

    console.log(`
  ✅ Done! Your HeadyMCP agent is ready.

  Next steps:
    cd ${agentName}
    npm run dev

  Register with Heady™Connection:
    heady agent register .
`);
}

main();
