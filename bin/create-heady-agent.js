'use strict';

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const TEMPLATES = {
    basic: {
        name: 'Basic CRUD Agent',
        description: 'File and data operations',
        tools: ['read_file', 'write_file', 'list_directory'],
    },
    'ai-assistant': {
        name: 'AI Assistant Agent',
        description: 'LLM-powered assistant',
        tools: ['ask_llm', 'summarize', 'translate', 'classify'],
    },
    'data-processing': {
        name: 'Data Processing Agent',
        description: 'CSV and JSON transformation',
        tools: ['parse_csv', 'parse_json', 'transform', 'export'],
    },
    integration: {
        name: 'Integration Agent',
        description: 'REST API client',
        tools: ['http_get', 'http_post', 'http_put', 'http_delete'],
    },
};

function generatePackageJson(agentName, template) {
    return JSON.stringify({
        name: agentName,
        version: '0.1.0',
        description: `HeadyMCP Agent: ${TEMPLATES[template].description}`,
        main: 'src/index.js',
        scripts: {
            dev: 'node src/index.js',
            start: 'node src/index.js',
            test: 'jest --coverage',
            lint: 'eslint src/',
        },
        keywords: ['heady', 'mcp', 'agent', template],
        dependencies: {
            '@modelcontextprotocol/sdk': '^1.0.0',
        },
        devDependencies: {
            jest: '^29.0.0',
            eslint: '^8.0.0',
        },
        engines: { node: '>=18.0.0' },
        license: 'MIT',
    }, null, 2);
}

function generateIndex(agentName, template) {
    const tools = TEMPLATES[template].tools;
    return `/**
 * ${agentName} — HeadyMCP Agent
 * Template: ${TEMPLATES[template].name}
 *
 * Generated by create-heady-agent
 */

'use strict';

const { Server } = require('@modelcontextprotocol/sdk/server/index.js');
const { StdioServerTransport } = require('@modelcontextprotocol/sdk/server/stdio.js');

const server = new Server(
  { name: '${agentName}', version: '0.1.0' },
  { capabilities: { tools: {}, resources: {} } }
);

// ─── Tools ────────────────────────────────────────────────────────

server.setRequestHandler('tools/list', async () => ({
  tools: [
${tools.map(t => `    {
      name: '${t}',
      description: 'TODO: Implement ${t}',
      inputSchema: { type: 'object', properties: {} },
    },`).join('\n')}
  ],
}));

server.setRequestHandler('tools/call', async (request) => {
  const { name, arguments: args } = request.params;

  switch (name) {
${tools.map(t => `    case '${t}':
      return { content: [{ type: 'text', text: 'TODO: Implement ${t}' }] };`).join('\n')}
    default:
      throw new Error(\`Unknown tool: \${name}\`);
  }
});

// ─── Resources ────────────────────────────────────────────────────

server.setRequestHandler('resources/list', async () => ({
  resources: [
    {
      uri: '${agentName}://status',
      name: 'Agent Status',
      description: 'Current status of ${agentName}',
      mimeType: 'application/json',
    },
  ],
}));

server.setRequestHandler('resources/read', async (request) => {
  if (request.params.uri === '${agentName}://status') {
    return {
      contents: [{
        uri: '${agentName}://status',
        mimeType: 'application/json',
        text: JSON.stringify({ status: 'running', uptime: process.uptime() }),
      }],
    };
  }
  throw new Error(\`Unknown resource: \${request.params.uri}\`);
});

// ─── Start ────────────────────────────────────────────────────────

async function main() {
  const transport = new StdioServerTransport();
  await server.connect(transport);
  console.error(\`${agentName} MCP server running on stdio\`);
}

main().catch(console.error);
`;
}

function generateTest(agentName) {
    return `/**
 * Tests for ${agentName}
 */

describe('${agentName}', () => {
  test('server starts without errors', () => {
    expect(() => require('../src/index')).not.toThrow();
  });

  test('exports MCP-compatible interface', () => {
    // Server should initialize
    expect(true).toBe(true);
  });
});
`;
}

