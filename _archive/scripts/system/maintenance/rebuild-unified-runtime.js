#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const rootDir = path.resolve(__dirname, '../../..');
const runtimeProfilePath = path.join(rootDir, 'configs/autonomy/unified-liquid-runtime.yaml');
const colabProfilePath = path.join(rootDir, 'configs/resources/colab-pro-plus-orchestration.yaml');
const templateRegistryPath = path.join(rootDir, 'configs/services/headybee-template-registry.yaml');
const snapshotPath = path.join(rootDir, 'configs/autonomy/unified-autonomy-snapshot.json');

function assert(condition, message) {
    if (!condition) throw new Error(message);
}

function parseRuntimeProfile(text) {
    const profile = (text.match(/^profile:\s*(.+)$/m) || [])[1]?.trim();
    const orchestrators = [...text.matchAll(/^\s*-\s*(heady-conductor|heady-cloud-conductor)\s*$/gm)].map((m) => m[1]);
    const swarmCoordinator = (text.match(/^\s*coordinator:\s*(.+)$/m) || [])[1]?.trim();
    const swarmWorkers = [...text.matchAll(/^\s*-\s*(headybees|headybee-template-registry)\s*$/gm)].map((m) => m[1]);
    const runtimeCells = [...text.matchAll(/^\s*-\s*id:\s*([a-z-]+)\s*$/gm)].map((m) => m[1]);
    const localComputePolicy = (text.match(/^\s*local_compute_policy:\s*(.+)$/m) || [])[1]?.trim();
    const maxSnapshotAgeMs = Number((text.match(/^\s*max_snapshot_age_ms:\s*(\d+)\s*$/m) || [])[1]);
    const deliveryModes = [...text.matchAll(/^\s*-\s*(live-system-projection|contextual-ui-stream|connector-manifest-stream)\s*$/gm)].map((m) => m[1]);

    return {
        profile,
        orchestrators,
        swarmCoordinator,
        swarmWorkers,
        runtimeCells,
        localComputePolicy,
        maxSnapshotAgeMs,
        deliveryModes,
    };
}

function parseColabProfile(text) {
    const workers = [...text.matchAll(/^\s*-\s*id:\s*(colab-[a-z])\s*$/gm)].map((m) => m[1]);
    return { workers };
}

function parseTemplateRegistry(text) {
    const objective = (text.match(/^\s*objective:\s*(.+)$/m) || [])[1]?.trim();
    const channels = new Set();
    for (const match of text.matchAll(/^\s*channels:\s*\[([^\]]+)\]\s*$/gm)) {
        const parts = match[1].split(',').map((value) => value.trim()).filter(Boolean);
        parts.forEach((part) => channels.add(part));
    }
    return { objective, channels: [...channels] };
}

function run() {
    const runtime = parseRuntimeProfile(fs.readFileSync(runtimeProfilePath, 'utf8'));
    const colab = parseColabProfile(fs.readFileSync(colabProfilePath, 'utf8'));
    const registry = parseTemplateRegistry(fs.readFileSync(templateRegistryPath, 'utf8'));

    assert(runtime.profile === 'heady-unified-liquid-runtime', 'Unified runtime profile missing or invalid.');
    assert(runtime.orchestrators.length >= 2, 'Both conductors must be registered.');
    assert(runtime.swarmCoordinator === 'headyswarm', 'HeadySwarm coordinator is required.');
    assert(runtime.swarmWorkers.includes('headybees'), 'HeadyBees worker mesh is required.');

    ['projection-cell', 'orchestration-cell', 'template-cell', 'creative-midi-cell'].forEach((requiredCell) => {
        assert(runtime.runtimeCells.includes(requiredCell), `Missing runtime cell: ${requiredCell}`);
    });

    assert(colab.workers.length === 3, 'Colab Pro+ orchestration must define exactly 3 workers.');
    assert(registry.channels.includes('headyswarm'), 'Template registry must publish to headyswarm.');
    assert(registry.channels.includes('headybees'), 'Template registry must publish to headybees.');

    const now = new Date().toISOString();
    const snapshot = {
        version: 2,
        generatedAt: now,
        description: 'Unified autonomy snapshot generated by deterministic runtime rebuild check',
        runtime: {
            initialized: true,
            startedAt: now,
            mode: runtime.profile,
            orchestration: {
                orchestrators: runtime.orchestrators,
                swarm: {
                    coordinator: runtime.swarmCoordinator,
                    workers: runtime.swarmWorkers,
                },
            },
            runtimeCells: runtime.runtimeCells,
        },
        templateRegistry: {
            channels: registry.channels,
            objective: registry.objective,
            validation: 'pass',
        },
        resources: {
            colabWorkers: colab.workers,
        },
        projection: {
            localComputePolicy: runtime.localComputePolicy,
            maxSnapshotAgeMs: runtime.maxSnapshotAgeMs,
            deliveryModes: runtime.deliveryModes,
        },
        maintenance: {
            lastAudit: now,
            checks: {
                unifiedRuntimeProfile: 'pass',
                colabThreePlanStrategy: 'pass',
                templateInjectionChannels: 'pass',
            },
        },
    };

    fs.writeFileSync(snapshotPath, `${JSON.stringify(snapshot, null, 2)}\n`);
    process.stdout.write(`${JSON.stringify({ ok: true, snapshotPath: path.relative(rootDir, snapshotPath) }, null, 2)}\n`);
}

run();
